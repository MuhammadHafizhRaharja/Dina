            <h1>About Bandung</h1>
            <p>Bandung, the capital city of West Java, is one of Indonesia’s 
                most captivating destinations, often referred to as the 
                “Paris van Java” due to its charm. The city offers a wide 
                range of attractions, from the lush highlands of Lembang and 
                Ciwidey, featuring Kawah Putih, Ranca Upas, and scenic tea 
                plantations, to urban gems like Dago Dream Park, Farmhouse 
                Lembang, and the historic streets of Braga and Asia Afrika. 
                In the heart of the city, Alun-Alun Bandung provides a vibrant 
                space surrounded by iconic landmarks such as the Grand Mosque 
                of Bandung.
                <br><br>
                Culinary delights are another highlight, with the city 
                boasting traditional Sundanese dishes like nasi timbel, 
                lotek, siomay, and batagor, alongside modern snacks such as s
                eblak, cireng, and the famous pisang bollen from Kartika Sari. 
                Bandung’s thriving coffee culture is evident in its numerous 
                artistic cafés in areas like Dago, Cihampelas, and Lembang, 
                making it a haven for food lovers.
                <br><br>
                Known for its innovation and creativity, Bandung is a 
                hub for technology, design, and art, with many startups 
                and creative communities fueling a dynamic work culture. 
                The city is also Indonesia's fashion capital, home to factory 
                outlets in Riau and Dago, local brands like House of Lea 
                and Damn! I Love Indonesia, and denim shops in Cihampelas.
                <br><br>
                Blessed with a cool climate thanks to its highland location, 
                Bandung enjoys average temperatures between 17°C and 27°C, 
                providing a comfortable atmosphere for outdoor activities. 
                As a City of Creativity and Innovation, Bandung frequently 
                hosts cultural, music, and tech festivals, emphasizing its 
                identity as a center of art and progress.
                <br><br>
                The people of Bandung are well-known for their warmth, 
                hospitality, and artistic spirit. They speak with a soft 
                tone, rooted in Sundanese culture, and are always ready 
                to assist visitors. This friendliness, combined with the 
                city’s natural beauty, rich culinary scene, and innovative 
                vibe, makes Bandung an irresistible destination for tourists 
                seeking diverse experiences.</p>
